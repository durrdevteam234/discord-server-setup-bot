function formatCute(text, style = 'wide', prefixEmoji = '🌸') {
    // Default back to normal text if style is 'off' or doesn't exist
    if (!style || style === 'off' || !fonts[style]) {
        return text;
    }

    const lowerText = text.toLowerCase();
    const currentFont = fonts[style];
    
    const styledText = lowerText
        .split('')
        .map(char => currentFont[char] || char)
        .join('');
        
    return `${prefixEmoji}﹒${styledText}`;
}

